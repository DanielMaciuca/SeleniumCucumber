package com.daniel.java.Constants;

public class MyClass {

    public static void main(String[] args) {

        // Constants are declared using the final keyword.
        // The final keyword is used to indicate that a variable's value cannot be changed after it has been assigned a value.
        // Constants are often declared with the combination of final and static keywords, making them class constants.

        final float pi = 3.14f;

        // pi = 1;

        System.out.println(pi);

    }
}
