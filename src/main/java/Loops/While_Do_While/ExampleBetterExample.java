package Loops.While_Do_While;

public class ExampleBetterExample {

    public static void main(String[] args) {
        int i = 6;

        while (i <= 5) {
            System.out.println("While loop: " + i);
            i++;
        }

        int y = 6;

        do {
            System.out.println("Do-while loop: " + y);
            y++;
        } while (y <= 5);
    }


}


