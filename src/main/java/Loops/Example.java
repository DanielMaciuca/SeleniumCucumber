package Loops;

public class Example {
    public static void main(String[] args) {

        for (int i = 1; i <= 5; i++) {
            System.out.println("Valoare lui i este " + i + "");
        }

        // Starting from 0

        for (int i = 0; i < 5; i++) {
            System.out.println("Valoare lui i este " + i + "");
        }

        // Looping backwards

        for (int i = 5; i >= 1; i--) {

            System.out.println("Valoare lui i este " + i + "");

        }
    }

}